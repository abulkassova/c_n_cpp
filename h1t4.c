#include <stdio.h>
int main(){
    int x= 17;
    int y=4;
    printf("the sum of x and y = %d\n", x+y);
    printf("the product of x and y = %d\n", x*y);
    printf("the difference between x and y (x-y) = %d\n", x-y);
    printf("the division of x and y = %f\n", (float)x/y);
    printf("the remainder of the division of x and y = %d\n", x%y);
    return 0;
}
